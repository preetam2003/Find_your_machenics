import prisma from '@/lib/prisma';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/Card';

async function getStats() {
  const [
    totalUsers,
    totalMechanics,
    pendingMechanics,
    totalBookings,
    recentBookings,
  ] = await Promise.all([
    prisma.user.count({ where: { role: 'USER' } }),
    prisma.mechanicShop.count({ where: { status: 'APPROVED' } }),
    prisma.mechanicShop.count({ where: { status: 'PENDING' } }),
    prisma.booking.count(),
    prisma.booking.count({
      where: {
        createdAt: {
          gte: new Date(Date.now() - 7 * 24 * 60 * 60 * 1000),
        },
      },
    }),
  ]);

  return {
    totalUsers,
    totalMechanics,
    pendingMechanics,
    totalBookings,
    recentBookings,
  };
}

